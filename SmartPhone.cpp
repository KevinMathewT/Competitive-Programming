ll n, a[500010];

void solve(){
	cin >> n;
	for(ll i=0;i<n;i++)
		cin >> a[i];

	sort(a, a + n);

	ll mx = -1;

	for(int i=0;i<n;i++)
		mx = max(mx, (n - i) * a[i]);

	cout << mx << "\n";
}

