const ll N = 100010, M = 1e9 + 7;
ll n, p, a[N];

void solve(ll cs){
	cin >> n >> p;
	for(ll i=0;i<n;i++) cin >> a[i];

	sort(a, a + n);

	ll tot = 0, mn = LLONG_MAX;
	ll p1 = 0;

	for(ll i=0;i<p;i++) tot += a[i];

	for(ll i=p-1;i<n;i++){
		mn = min(mn, p * a[i] - tot);
		if(i == n - 1) break;
		tot += a[i + 1];
		tot -= a[p1++];
	}

	cout << "Case #" << (cs + 1) << ": " << mn << "\n";
}

