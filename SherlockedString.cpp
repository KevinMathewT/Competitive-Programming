const ll N = 30, M = 1e9 + 7;
string s;
ll f1[N], f2[N];

void solve(){
	fill(f1, f1 + N, -1);
	fill(f2, f2 + N, -1);

	cin >> s;

	for(ll i=0;i<s.size();i++){
		if(f1[s[i] - 'a'] == -1) f1[s[i] - 'a'] = i;
		f2[s[i] - 'a'] = i;
	}

	ll mx = 0;
	for(ll i=0;i<26;i++)
		mx = max(mx, abs(f1[i] - f2[25 - i]));

	cout << mx << "\n";
}

