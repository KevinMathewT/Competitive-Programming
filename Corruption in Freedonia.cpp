const ll N = 1000010, M = 1e9 + 7;
ll n;
vector<ll> T[N];

ll dfs(ll u, ll p = -1){
	if(T[u].size() == 0 || T[u].size() == 1) return 1;
	ll flag = 1, c = 0;
	for(ll v : T[u])
		if(v != p){
			ll z = dfs(v, u);
			if(z != 1) flag = 0;
			c += z;
		}

		if(flag) return c - 1;
		else return c;
}

void solve(){
	cin >> n;
	for(ll i=0;i<n-1;i++){
		ll u, v; cin >> u >> v; u--; v--;
		T[u].push_back(v);
		T[v].push_back(u);
	}

	cout << dfs(0) << "\n";
}

