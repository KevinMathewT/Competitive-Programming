const ll N = 50, M = 1e9 + 7;
ll t, n, a[N];

void solve(){
	cin >> n;
	for(ll i=0;i<n;i++) cin >> a[i];

	vector<ll> ret;
	ll mx = 0;

	for(ll i=0;i<(1LL << n);i++){
		ll tot = 0;
		for(ll j=0;j<n;j++)
			if((i & (1LL << j)))
				tot += a[j];

		if(tot > t) continue;
		if(tot > mx){
			mx = tot;
			ret = vector<ll>(0);
			for(ll j=0;j<n;j++)
				if((i & (1LL << j)))
					ret.push_back(a[j]);
		}
	}

	for(ll i=0;i<ret.size();i++) cout << ret[i] << ' ';
	cout << "sum:" << mx << "\n";
}

