string s;

void solve(){
	cin >> s;
	ll z = 0, t = 0, e = 0, p = -1;
	for(ll i=0;i<s.size();i++){
		if(s[i] == '0') z++, p = i;
		t += s[i] - '0';
	}

	for(ll i=0;i<s.size();i++)
		if(i != p && (s[i] - '0') % 2 == 0) e++;

	if(z >= 1 && e >= 1 && t % 3 == 0) cout << "red\n";
	else cout << "cyan\n";
}

