const ll N = 100010, M = 1e9 + 7;
ll n, a[N], p[N], s;

void solve(){
	cin >> n >> s;
	for(ll i=0;i<n;i++) cin >> a[i];
	p[0] = a[0];
	for(ll i=1;i<n;i++) p[i] = p[i-1] + a[i];

	ll mx = 0, tot = 0, g = 0, sk, mxi;

	for(ll i=0;i<n;i++){
		if(a[i] > mx) mx = a[i], mxi = i;
		if(p[i] <= s) g = max(g, i + 1), sk = 0;
		else if(p[i] - mx <= s) max(g, i), sk = mxi + 1;
	}

	cout << sk << "\n";
}

