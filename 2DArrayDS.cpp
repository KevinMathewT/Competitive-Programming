ll a[6][6];

void solve(){
	for(ll i=0;i<6;i++)
		for(ll j=0;j<6;j++)
			cin >> a[i][j];

	ll mx = LLONG_MIN;
	for(ll i=0;i<4;i++)
		for(ll j=0;j<4;j++)
			mx = max(mx, a[i][j] + a[i][j+1] + a[i][j+2] + a[i+1][j+1] + a[i+2][j] + a[i+2][j+1] + a[i+2][j+2]);

	cout << mx << "\n";
}

