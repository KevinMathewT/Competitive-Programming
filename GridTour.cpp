ll n, m, k;

void solve(){
	cin >> n >> m >> k;

	if(n > m)
		swap(n, m);

	if(__gcd(n, k) == 1 && __gcd(m, k) == 1)
		cout << (n * m) << "\n";
	else
		cout << -1 << "\n";
}

