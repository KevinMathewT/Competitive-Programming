const ll N = 1010, M = 1e9 + 7;
ll n, m, dp[10][N][N], dp1[N][N];

ll DP1(ll x, ll d){
	if(d < 0) return 0;
	if(dp1[x][d] != -1) return dp1[x][d];
	if(x == m) return dp1[x][d] = (((d + 1) * (d + 2)) / 2) % M;

	dp1[x][d] = 0;
	for(ll i=0;i<=d;i++)
		dp1[x][d] = (dp1[x][d] + ((d - i + 1) * DP1(x + 1, i))) % M;

	return dp1[x][d];
}

void solve(){
	cin >> n >> m;

	for(ll i=0;i<N;i++)
		for(ll j=0;j<N;j++)
			dp1[i][j] = -1;

	cout << DP1(1, n - 1);
}

