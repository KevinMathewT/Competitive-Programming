const ll N = 100010, M = 1e9 + 7;
ll n, sieve[N];
vector<ll> p;

void solve(){
	cin >> n;
	vector<ll> r(0);
	for(ll i : p)
		if(i > n) break;
		else r.push_back(i);

	ll comp = 1;
	for(ll i=1;i<r.size();i++)
		if(r[i] * r[0] > n){
			comp += r.size() - i;
			break;
		}

	cout << comp << "\n";
}

