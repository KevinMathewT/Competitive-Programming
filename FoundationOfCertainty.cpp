ll n;

ll phi(ll n) { 
    ll result = 1; 
    for (ll i = 2; i < n; i++) 
        if (__gcd(i, n) == 1) 
            result++; 
    return result; 
}

ll calc(ll x){
	ll c = 0;
	for(ll i=1;i<=x;i++)
		if(x % i == 0) c++;
	return c;
}

