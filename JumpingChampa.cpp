const ll N = 1010, M = 1e9 + 7;
ll n, q, h[N];

void solve(){
	cin >> n >> q;
	for(ll i=0;i<n;i++) cin >> h[i];
	sort(h, h + n);

	ll tot = 0;
	for(ll i=1;i<n;i++)
		tot += h[i] - h[i - 1];

	cout << q * tot << "\n";
}

