ll n, k;

void solve(){
	cin >> n >> k;

	if(k > n){
		cout << 0 << "\n";
		return;
	}
	if(k == n){
		cout << 1 << "\n";
		return;
	}

	cout << (((n - k + 1) * (n - k + 2)) / 2) << "\n";
}

