const ll N = 1000010, M = 1e9 + 7;
ll sieve[N], b[N], n, m, q, dp[N];

void solve(ll t){
	cin >> n >> m >> q;
	fill(dp, dp + n + 10, -1);
	fill(sieve, sieve + n + 10, 0);
	for(ll i=0;i<m;i++) cin >> b[i];

	for(ll i=0;i<m;i++)
		sieve[b[i]] = 1;

	ll ans = 0;

	while(q--){
		ll x; cin >> x;
		if(dp[x] != -1) {
			ans += dp[x];
			continue;
		}

		ll tot = 0;

		for(ll k=x;k<=n;k+=x)
			if(sieve[k] == 0)
				tot++;

		dp[x] = tot;
		ans += dp[x];
	}

	cout << "Case #" << t << ": " << ans << "\n";
}

