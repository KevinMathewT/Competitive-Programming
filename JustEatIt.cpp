const ll N = 100010, M = 1e9 + 7;
ll n, a[N], dp[N];

void solve(){
	cin >> n;
	for(ll i=0;i<n;i++) cin >> a[i];
	dp[0] = a[0];
	ll flag = 1;
	for(ll i=1;i<n;i++){
		dp[i] = max(a[i], a[i] + dp[i-1]);
		if(dp[i] == a[i]) flag = 0;
	}

	ll tot = accumulate(a, a + n, (ll)0);
	ll mx = *max_element(dp, dp + n - 1);
	ll sum = 0;
	for(ll i=n-1;i>0;i--)
		sum += a[i], mx = max(mx, sum);

	if(tot > mx) cout << "YES\n";
	else cout << "NO\n";
}

