const ll N = 110, M = 110;
ll n, m, a[N][M], ag[N];

void solve(){
	fill(ag, ag + N, 0);
	cin >> n >> m;

	for(ll i=0;i<m;i++){
		for(ll j=0;j<n;j++){
			cin >> a[i][j];
		}

		ll tot = accumulate(a[i], a[i] + n, 0LL);
		ld avg = tot;
		avg /= n;

		for(ll j=0;j<n;j++)
			if(a[i][j] > avg)
				ag[j] = 1;
	}

	cout << accumulate(ag, ag + n, (ll)0) << "\n";
}

