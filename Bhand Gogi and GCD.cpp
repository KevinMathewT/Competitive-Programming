const ll N = 10010, M = 1e9 + 7;
ll n, a[N], p[N], s[N];

void solve(){
	cin >> n;
	for(ll i=0;i<n;i++) cin >> a[i];
	p[0] = a[0];
	for(ll i=1;i<n;i++) p[i] = __gcd(a[i], p[i - 1]);
	s[n-1] = a[n-1];
	for(ll i=n-2;i>=0;i--) s[i] = __gcd(a[i], s[i + 1]);

	cout << max(*max_element(p, p + n), *max_element(s, s + n)) << "\n";
}

