const ll N = 100010, M = 1e9 + 7;
ll n, x, y, a[N];

void solve(){
	cin >> n >> x >> y;
	for(ll i=0;i<n;i++) cin >> a[i];

	ll l = 1, r = 1e10 + 10, poss = -1;

	while(l <= r){
		ll z = (l + r) >> 1;

		ll cost = 0;
		for(ll i=0;i<n;i++)
			if(a[i] < z) cost += (z - a[i]) * y;
			else cost -= (a[i] - z) * x;

		if(cost < 0) l = z + 1;
		else{
			poss = cost;
			r = z - 1;
		}
	}

	cout << poss << "\n";
}

