const ll N = 1e8, M = 1e9 + 7;

void sieve(vector<ll> &primes)
{
 vector<bool> mark(N + 2, 1);
 primes.emplace_back(2);   //emplace_back is faster than v.push_back
 for(ll i=3;i<=N;i+=2)           //Iterating just over the odd numbers since, no even numbers other than 2 can be prime 
 {
     if(mark[i])
     {
          primes.emplace_back(i);
         for(ll j=i*i;j<=N;j+=2*i) 
             mark[j]=0;       
     }
 }
}

void solve(){
	vector<ll> primes;
	sieve(primes);
}

