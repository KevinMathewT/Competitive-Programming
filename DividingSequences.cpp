ll n, a[10010], dp[10010];

void solve(){
	cin >> n;
	for(ll i=0;i<n;i++)
		cin >> a[i];
	fill(dp, dp + n, 1);
	dp[0] = 1;
	for(ll i=1;i<n;i++)
		for(ll j=i-1;j>=0;j--)
			if(a[i] % a[j] == 0)
				dp[i] = max(dp[i], 1 + dp[j]);

	cout << *max_element(dp, dp + n) << "\n";
}

