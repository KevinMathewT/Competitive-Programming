const ll N = 100010, M = 1e9 + 7;
ll k, a, b, dp[N];

void solve(){
	cin >> a >> b;
	cout << (dp[b] - dp[a - 1] + M) % M << "\n";
}

int main(){
	// freopen("input.txt", "r", stdin);		//Comment
	// freopen("output.txt", "w", stdout);		//this out.
	ios::sync_with_stdio(false);			//Not
	cin.tie(NULL);							//this.
	cout.tie(0);							//or this.

	ll T;
	cin >> T >> k;

	dp[0] = 1;
	for(ll i=1;i<N;i++)
		dp[i] = (dp[i-1] + (((i - k) >= 0) ? dp[i - k] : 0)) % M;
	for(ll i=1;i<N;i++)
		dp[i] = (dp[i] + dp[i - 1]) % M;

	while(T--)
		solve();

	return 0;
}