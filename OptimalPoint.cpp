ll n, k, a[10][100010];

void solve(){
	cin >> n >> k;
	for(ll i=0;i<n;i++)
		for(ll j=0;j<k;j++)
			cin >> a[j][i];

	for(ll i=0;i<k;i++){
		sort(a[i], a[i] + n);
		cout << a[i][(n / 2)] << " ";
	}
	cout << "\n";
}

