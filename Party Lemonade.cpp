const ll N = 35, M = 1e9 + 7;
ll n, L, c[N];

ll getVal(ll x) {
	ll l = 0, r = 0;
	while ((1LL << l) <= x) {
		if ((1LL << l) & x) r += c[l];
		l++;
	}

	return r;
}

