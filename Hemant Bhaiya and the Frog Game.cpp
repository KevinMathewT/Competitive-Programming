const ll N = 100010, M = 1e9 + 7;
ll n, h[N], x[N], y[N], dp[N], c[N];

void solve(){
	cin >> n;
	for(ll i=0;i<n;i++) cin >> h[i];
	for(ll i=0;i<n;i++) cin >> x[i];
	for(ll i=0;i<n;i++) cin >> y[i];
	for(ll i=0;i<n;i++) cin >> c[i];

	dp[n-1] = c[n-1];
	for(ll i=n-2;i>=0;i--){
		ll mx = 0;
		for(ll j=i + x[i];j<=n - 1;j++)
			if(h[j] >= h[i] && h[j] <= h[i] + y[i]) mx = max(mx, dp[j]);
		dp[i] = c[i] + mx;
	}

	cout << *max_element(dp, dp + n) << "\n";
}

