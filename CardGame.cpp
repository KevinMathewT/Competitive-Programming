ll n, k1, k2;

void solve(){
	cin >> n >> k1 >> k2;

	ll mx1 = LLONG_MIN;
	for(ll i=0;i<k1;i++){
		ll x; cin >> x;
		mx1 = max(mx1, x);
	}

	ll mx2 = LLONG_MIN;
	for(ll i=0;i<k2;i++){
		ll x; cin >> x;
		mx2 = max(mx2, x);
	}

	// cout << mx1 << ' ' << mx2 << '\n';

	if(mx1 > mx2) cout << "YES\n";
	else cout << "NO\n";
}

