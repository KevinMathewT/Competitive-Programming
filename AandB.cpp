ll a, b;

void solve(){
	cin >> a >> b;
	if(a < b) swap(a, b);

	a -= b;
	if(a == 0){
		cout << 0 << "\n";
		return;
	}

	ll p = (-1 + sqrt(1 + 8 * a)) / 2;

	if((p * (p + 1)) / 2 != a){
		p++;
		while((a - ((p * (p + 1)) / 2)) % 2) p++;
	}

	cout << p << '\n';
}

