ll n;

void solve(){
	cin >> n;

	if(n % 2 == 0){
		cout << 5 << "\n";
		cout << 0 << ' ' << 0 << "\n";
		cout << 0 << ' ' << n << "\n";
		cout << n << ' ' << 0 << "\n";
		cout << n << ' ' << n << "\n";
		cout << n / 2 << ' ' << n / 2 << "\n";
	}

	else{
		cout << 4 << "\n";
		cout << 0 << ' ' << 0 << "\n";
		cout << 0 << ' ' << n << "\n";
		cout << n << ' ' << 0 << "\n";
		cout << n << ' ' << n << "\n";
	}
}

