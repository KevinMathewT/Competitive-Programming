ll n;
vector<ll> a;

void solve(){
	a.clear();
	cin >> n;
	a = vector<ll>(n, 0);
	for(ll i=0;i<n;i++)
		cin >> a[i];

	ll i = 0, m = 0;

	m = accumulate(a.begin(), a.end(), 0LL);

	for(ll j=n;j<5*n;j++){
		a.push_back(m / n);
		m -= a[i++];
		m += a[j];
	}

	cout << a << "\n";
}

