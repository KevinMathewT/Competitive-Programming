const ll N = 1, M = 1e9 + 7;
ll l, r;

ll nextG(ll x, ll p){
	ll z = (1LL << p);
	return (((((x / z) * z)) + z - 1));
}

void solve(){
	cin >> l >> r;
	ll sum = 0;

	for(ll i=0;i<64;i++){
		if(!(l & (1LL << i))) continue;
		ll range = ((nextG(l, i) - (l % M) + 1LL + M));
		if(nextG(l, i) > r) range = (r - l + 1) % M;
		sum = (sum + ((range % M) * ((1LL << i) % M)) % M) % M;
	}

	cout << sum << "\n";
}

