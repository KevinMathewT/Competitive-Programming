string s;

void solve(){
	cin >> s;
	if(s.size() % 2 == 0){
		cout << "Non-inhabitant\n";
		return;
	}

	ll n = s.size();
	cout << "The inhabitant " << s << " can have " << ((n / 2 + 1) * (n / 2 + 1)) << " children\n";
}

