void getBin(ll x){
	vector<ll> bts(0);

	while(x > 0){
		bts.push_back(x % 2);
		x /= 2;
	}

	reverse(bts.begin(), bts.end());
	cout << bts << "\n";
}

void solve(){
	int m = 10;
	int s = m;
	while (s > 0) {
		getBin(s);
	 	s = (s-1) & m;
	}
}

