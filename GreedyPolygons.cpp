const ll N = 110, M = 1e9 + 7;
ll n, l, d, g;

void solve(){
	cin >> n >> l >> d >> g;
	ld area = ((l * l) * n) / (4 * tan(PI / (n)));
	for(ll i=0;i<n;i++) area = area + (g * d * l);
	area += PI * (g * g * d * d);
	cout << area << "\n";
}

