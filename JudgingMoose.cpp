ll n, m;

void solve(){
	cin >> n >> m;

	if(n == 0 && m == 0){
		cout << "Not a moose\n";
		return;
	}

	if(n == m){
		cout << "Even " << (n + m) << "\n";
		return;
	}

	cout << "Odd " << (2 * max(n, m)) << "\n";
}

