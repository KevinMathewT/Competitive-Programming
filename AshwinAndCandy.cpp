ll n;

void solve(){
	cin >> n;
	if(n % 2 != 0 || n <= 4)
		cout << 0 << "\n";
	else if((n / 2) % 2 == 1)
		cout << max((ll)0, n / 4) << "\n";
	else
		cout << max((ll)0, n / 4 - 1) << "\n";
}

