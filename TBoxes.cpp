ll n;
priority_queue<ll> a, b;

void solve(){
	cin >> n;

	for(ll i=0;i<n;i++){
		ll c;
		cin >> c;
		a.push(c);
		b.push(-c);

		cout << (a.top() + (-b.top())) << "\n";
	}
}

